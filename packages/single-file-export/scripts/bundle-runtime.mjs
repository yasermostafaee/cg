#!/usr/bin/env node
// Bundles @cg/template-runtime into `src/generated/cg-runtime-bundles.ts` — a
// generated TS module exporting the runtime as two strings:
//
//   cgJs      ESM  (es2022) — the preview iframe + the .vcg (modern browsers).
//   cgJsIife  IIFE (chrome71) — the file://-safe single-file CasparCG export
//                               (D-019). Targets the oldest supported CEF
//                               (CasparCG 2.3 LTS ≈ Chromium 71) and exposes
//                               `window.CG = { createRuntime, installCasparGlobals, … }`.
//
// Emitting a TS module of string consts (vs a Vite `?raw` import) keeps the
// package bundler-agnostic — it builds to plain `dist` consumed by both apps.
//
// Same @cg/template-runtime source feeds both — only the bundle format/target
// differs, so what the Designer shows IS what CasparCG plays. Regenerated by the
// package's pre{build,typecheck,test} hooks; gitignored. Write is atomic.

import * as esbuild from 'esbuild';
import * as fs from 'node:fs';
import * as path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const PKG_ROOT = path.resolve(__dirname, '..');
const REPO_ROOT = path.resolve(PKG_ROOT, '..', '..');
const ENTRY = path.resolve(REPO_ROOT, 'packages/template-runtime/src/index.ts');
const OUT_DIR = path.resolve(PKG_ROOT, 'src/generated');
const OUT_FILE = path.join(OUT_DIR, 'cg-runtime-bundles.ts');

/** Bundle the runtime once and return its source text. */
async function bundle({ format, target, globalName }) {
  const result = await esbuild.build({
    entryPoints: [ENTRY],
    bundle: true,
    format,
    platform: 'browser',
    target,
    ...(globalName !== undefined ? { globalName } : {}),
    write: false,
    minify: false,
    sourcemap: false,
  });
  const out = result.outputFiles[0];
  if (!out) throw new Error(`esbuild produced no output for ${format}/${target}`);
  return out.text;
}

const cgJs = await bundle({ format: 'esm', target: 'es2022' });
const cgJsIife = await bundle({ format: 'iife', target: 'chrome71', globalName: 'CG' });

// JSON.stringify yields a valid, fully-escaped JS string literal — bundle text
// (backticks, `${`, backslashes, newlines) survives intact and byte-identical.
const contents =
  '// GENERATED by scripts/bundle-runtime.mjs — DO NOT EDIT.\n' +
  '// Regenerated by the package pre{build,typecheck,test} hooks.\n' +
  '/* eslint-disable */\n' +
  `export const cgJs = ${JSON.stringify(cgJs)};\n` +
  `export const cgJsIife = ${JSON.stringify(cgJsIife)};\n`;

await fs.promises.mkdir(OUT_DIR, { recursive: true });
// Atomic write: temp + rename so a concurrent reader never sees a partial file.
const tmp = `${OUT_FILE}.tmp-${String(process.pid)}`;
await fs.promises.writeFile(tmp, contents, 'utf-8');
await fs.promises.rename(tmp, OUT_FILE);

const kb = (s) => (Buffer.byteLength(s, 'utf-8') / 1024).toFixed(1);
console.error(
  `[bundle-runtime] wrote ${path.relative(PKG_ROOT, OUT_FILE)} (cgJs ${kb(cgJs)} KB, cgJsIife ${kb(cgJsIife)} KB)`,
);
